import Together from 'together-ai';

const together = new Together({ 
    apiKey: import.meta.env.VITE_TOGETHER_API_KEY 
});

export async function generateImage(prompt) {
    try {
        const response = await together.images.create({
            model: "black-forest-labs/FLUX.1-schnell-Free",
            prompt: `high quality seamless texture, tileable pattern, professional material texture, ${prompt}, detailed surface, high resolution texture map, professional material design, clear material definition, photorealistic texture, seamless tiling texture, professional texture quality`,
            width: 1024,
            height: 1024,
            steps: 4,
            n: 1,
            response_format: "b64_json"
        });
        
        return response.data[0].b64_json;
    } catch (error) {
        console.error('Error generating texture:', error);
        throw error;
    }
}